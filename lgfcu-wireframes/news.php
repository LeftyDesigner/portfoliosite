<p>
Data Privacy Day, celebrated on January 28, is an international day of awareness to educate everyone to respect privacy and safeguard personal information. 
</p>

<p>
The National Cyber Security Alliance (NCSA) and Data Privacy Day partners aim to help Internet users understand and control their online experience and online reputation. To accomplish this mission, NCSA, a non-profit public-private partnership focused on helping all digital citizens stay safer and more secure online and official coordinator of Data Privacy Day (DPD), is collaborating with many educational institutions, corporations, government and non-profit organizations across the world, to make Data Privacy Day a success.
</p>

<p>Here are a few ideas to help you keep your data private:</p>

<ul>
<li><strong>Secure your devices</strong>. Keep your devices from prying eyes. Set passcodes or pass phrases (long passwords) to be sure only you can access your smartphone, tablet or PC.</li>
<li><strong>Secure your accounts</strong>. Passwords are no longer the only protection from would-be hackers. Enable two-factor authentication to add another layer of security.</li>
<li><strong>Make passwords long, strong and unique</strong>. Passwords should be different for each account, have as many characters as allowed and include numbers, symbols and letters, capital and lowercase.</li> 
<li><strong>Think before you app</strong>. Before downloading a mobile app, understand what information (your location, access to social networks, etc.) the app accesses to function.</li>
<li><strong>Back it up</strong>. Store digital copies of your valuable work, music, photos and other information on an external hard drive or online cloud.</li>
<li><strong>Check your privacy settings</strong>. If you need help, <a href="https://www.staysafeonline.org/data-privacy-day/check-your-privacy-settings/">here is a list</a> of common websites and how to view or change the privacy/security settings.</li>
<li><strong><a href="https://www.staysafeonline.org/data-privacy-day/parent-resources/">Discuss privacy with your family</a></strong>. Have a “privacy talk” with your family, reminding everyone not to overshare personal information online.</li> <li>Discuss other tips for using mobile devices and social networks responsibly. Use Data Privacy Day resources to guide the conversation.</li>
<li><strong><a href="https://www.staysafeonline.org/data-privacy-day/privacy-library/">Share privacy resources</a></strong>. Print privacy tip sheets to display around the home computer or office, and other public gathering places like libraries and community centers.</li>
</ul>


<p>
Anyone can get involved and help raise awareness about the importance of data privacy. We all share in the responsibility to make the Internet a safer place. Learn more about how you can stay safe online at <a href="http://www.staysafeonline.org"><strong>www.staysafeonline.org</strong></a>.
</p>

