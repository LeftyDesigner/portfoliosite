<h3>Our vision</h3>
<p>
To promote thrift among members by affording them an opportunity to accumulate their savings and to create for them a source of credit for provident or productive purposes. By improving members' lives, LGFCU believes the communities in which they live across the state of North Carolina will also become better places to live, work and play.
Our values

Local Government Federal Credit Union is committed to distinctive quality and unparalleled member service in all aspects of our business. Driving everything we do is a set of powerful ideas that make up the credit union philosophy. These ideas are enduring. They've worked successfully, virtually unchanged, for a more than a century. They're also universal, effective in all kinds of communities, and in different types of political and economic systems in countries all over the world.

Yet, the ideas that form the core of our philosophy are also simple. They're all based on one strong belief: People are more important than dollars.
</p>

