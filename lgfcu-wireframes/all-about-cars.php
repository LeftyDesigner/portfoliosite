			<h3 id="buycar">How to Buy a Car <span class="right tinytxt"><a href="#pgtop">Back to top <span class="pictogram">&#9652;</span></a></h3>
		</div>
		<div class="small-6 large-4 left column">
			<p><strong>Aside from a home, a car is one of the largest purchases you’re likely to make. This is a decision you want to get right, so it’s important not to leave anything to chance. But where do you start? </strong></p>
			
				<ol>
					<li><strong>Know Your Needs</strong> - Whether it’s gas mileage or towing capacity, room for a carpool or the latest in safety features, determine what you need and then use that information to find the best fit for your lifestyle.</li>

					<li><strong>New or Used?</strong> - New cars can come at a high price, and while you’ll save money on the purchase price of a used car, it may finance at a higher interest rate. Make a decision that works for you.</li>

					<li><strong>How Much Can You Afford?</strong> - Your car payment shouldn’t exceed 20 percent of your monthly take-home pay.</li>

					<li><strong>Hit the Books</strong> - Read everything you can find about a specific car and ask around. Chances are, someone you know is driving the car you’re interested in.</li>

					<li><strong>Financing</strong> - Dealership financing is not likely to be the rock-bottom APR you see advertised in the lobby. Odds are, your Credit Union can make a much better offer. Knowing your credit score and what interest rate you qualify for is another way to protect yourself and make an informed decision.</li>

					<li><strong>Test Driving</strong> - Drive at highway speeds and in stop-and-go traffic for daily commutes. For a family car, make sure your kids have room in the backseat, especially if you have little ones still in car seats. Test the brakes. Turn corners sharply. If you feel uneasy about a car, walk away.</li>

					<li><strong>The Art of Negotiation</strong> - Knowledge is power. From your research, you know what the fair price is for the car you want. Be prepared to walk away if a dealer can’t meet your price. Start at the invoice price (new car) or the wholesale price (used car) and negotiate up (never down). Go with the dealer offering the best customer service.</li>

					<li><strong>Trade-Ins</strong> - You’ll likely make more money selling your car yourself, but if you do decide to sell it to the dealership, don’t fly blind. Know what your car is worth, and keep an eye on the numbers. Make sure the dealer isn’t offering you a generous trade-in allowance, while boosting the selling price of the new car to compensate.</li>
				</ol>
				
